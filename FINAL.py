    avg_cb = df_c['cashback_intensity'].mean()
    avg_sv = df_c['scratch_velocity'].mean()
    avg_fp = df_c['full_price_rate'].mean()
    avg_jc = df_c['j_curve_ratio'].mean()
    
    # Trace specific feature traits dynamically
    if avg_fp > df_features['full_price_rate'].median() and avg_cb < df_features['cashback_intensity'].median():
        b_name = "Organic Value Anchors"
