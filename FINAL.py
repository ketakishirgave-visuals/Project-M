for cid in sorted(df_features['cluster_id'].unique()):
    df_c = df_features[df_features['cluster_id'] == cid]
    total = len(df_c)
    
    st_p = (y_true[df_c.index] == 'sure_thing').sum() / total * 100
    h_p  = (y_true[df_c.index] == 'hunter').sum() / total * 100
    as_p = (y_true[df_c.index] == 'accidental_saver').sum() / total * 100
    
    # Calculate dominant category purely for mathematical tracking
    dom = max([('Sure Thing', st_p), ('Hunter', h_p), ('Accidental Saver', as_p)], key=lambda x: x[1])[0]
    val_data.append([f"Cluster {cid}", total, f"{st_p:.1f}%", f"{h_p:.1f}%", f"{as_p:.1f}%", dom])

df_val = pd.DataFrame(val_data, columns=['Cluster ID', 'Total Count', 'Sure Thing %', 'Hunter %', 'Accidental Saver %', 'Dominant Archetype Mapping'])
print(df_val.to_string(index=False))

