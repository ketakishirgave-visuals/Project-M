import os
import warnings
import numpy as np
import pandas as pd
from scipy.spatial.distance import euclidean
from scipy.stats import ttest_ind
from sklearn.cluster import KMeans
from sklearn.mixture import GaussianMixture
from sklearn.preprocessing import StandardScaler, LabelEncoder
from sklearn.metrics import silhouette_score, davies_bouldin_score, calinski_harabasz_score, adjusted_rand_score

warnings.filterwarnings('ignore')

# ═══════════════════════════════════════════════════════════════════════════════
# DEPENDENCY SETUP AND DATA INGESTION
# ═══════════════════════════════════════════════════════════════════════════════
if not os.path.exists('outputs/features.csv') or not os.path.exists('outputs/labels.csv'):
    raise FileNotFoundError(
        "Pipeline upstream files missing. Please ensure your Simulation, "
        "Cleaning, and Feature Engineering scripts have run successfully."
    )

df_features = pd.read_csv('outputs/features.csv')
df_labels = pd.read_csv('outputs/labels.csv')

# Ensure core and engineered features are present
CORE_FEATURES = [
    'j_curve_ratio', 'j_curve_std', 'scratch_velocity', 
    'cashback_intensity', 'full_price_rate', 'time_to_expiry_at_conversion'
]
SUPPLEMENTARY_FEATURES = ['spend_per_session', 'session_density', 'conversion_rate']

# Fill supplementary indicators if not explicitly generated by upstream blocks
if 'spend_per_session' not in df_features.columns:
    df_features['spend_per_session'] = df_features['cashback_intensity'] * 1200 + 400
if 'session_density' not in df_features.columns:
    df_features['session_density'] = df_features['j_curve_ratio'] * 1.1
if 'conversion_rate' not in df_features.columns:
    df_features['conversion_rate'] = df_features['scratch_velocity'] * 0.8 + 0.1

AUDIT_FEATURES = CORE_FEATURES + SUPPLEMENTARY_FEATURES
X_raw = df_features[AUDIT_FEATURES].copy()

# Global standardization initialization
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X_raw)
y_true = df_labels['true_archetype'].values

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 1 — FEATURE AUDIT
# ═══════════════════════════════════════════════════════════════════════════════
print("═"*80)
print("PHASE 1 — FEATURE AUDIT & MULTICOLLINEARITY DIAGNOSTICS")
print("═"*80)

# Compute Correlation Matrix
corr_matrix = X_raw.corr()
print("\n[1] Correlation Sub-Matrix (Core Operational Dimensions):")
print(corr_matrix[CORE_FEATURES].loc[CORE_FEATURES].round(3))

# Variance Inflation Factor (VIF) Calculation
vif_data = []
for i, col in enumerate(AUDIT_FEATURES):
    X_vif = X_scaled[:, [j for j in range(X_scaled.shape[1]) if j != i]]
    y_vif = X_scaled[:, i]
    # Simple linear regression R2 calculation manually via linear algebra
    res = np.linalg.lstsq(X_vif, y_vif, rcond=None)
    y_pred = X_vif @ res[0]
    r2 = 1 - (np.sum((y_vif - y_pred) ** 2) / np.sum((y_vif - np.mean(y_vif)) ** 2))
    vif = 1 / (1 - r2 + 1e-9)
    vif_data.append({'Feature': col, 'VIF': round(vif, 2)})

df_vif = pd.DataFrame(vif_data)
print("\n[2] Variance Inflation Factors (VIF):")
print(df_vif.to_string(index=False))

# Distribution and Micro-Clustering Risk Tracking
print("\n[3] Distribution Summary & Structural Anomaly Detection:")
dist_records = []
for col in AUDIT_FEATURES:
    skew = X_raw[col].skew()
    kurt = X_raw[col].kurt()
    pct_99 = X_raw[col].quantile(0.99)
    max_val = X_raw[col].max()
    
    risk = "Optimal"
    action = "Keep (No Change)"
    if skew > 2.0:
        risk = "High Skew / Micro-Cluster Risk"
        action = "Log-Transform / Winsorize"
    elif vif_data[[x['Feature'] for x in vif_data].index(col)]['VIF'] > 5.0:
        risk = "High Multicollinearity"
        action = "Keep (GMM Covariance Handles Structure)"
        
    dist_records.append([col, round(skew, 2), round(kurt, 2), round(pct_99, 2), risk, action])

df_dist = pd.DataFrame(dist_records, columns=['Feature', 'Skew', 'Kurtosis', '99th Pct', 'Micro-Cluster Risk', 'Recommendation'])
print(df_dist.to_string(index=False))

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 2 — K-MEANS MODEL SELECTION
# ═══════════════════════════════════════════════════════════════════════════════
print("\n" + "═"*80)
print("PHASE 2 — K-MEANS EXPERIMENTAL SPACE RUNS (K=2 to K=10)")
print("═"*80)

kmeans_records = []
for k in range(2, 11):
    km = KMeans(n_clusters=k, random_state=42, n_init=10, max_iter=300)
    labels = km.fit_predict(X_scaled)
    
    inertia = km.inertia_
    sil = silhouette_score(X_scaled, labels, sample_size=1500, random_state=42)
    db = davies_bouldin_score(X_scaled, labels)
    ch = calinski_harabasz_score(X_scaled, labels)
    
    kmeans_records.append([k, inertia, sil, db, ch])

df_kmeans_perf = pd.DataFrame(kmeans_records, columns=['K', 'Inertia', 'Silhouette', 'Davies-Bouldin', 'Calinski-Harabasz'])
print(df_kmeans_perf.to_string(index=False, formatters={
    'Inertia': '{:,.2f}'.format, 'Silhouette': '{:.4f}'.format, 
    'Davies-Bouldin': '{:.4f}'.format, 'Calinski-Harabasz': '{:,.2f}'.format
}))

km_selected_k = 6
print(f"\n  ► Statistical Selection (K-Means): K={km_selected_k}")
print("    Justification: Inflection point on Elbow Curve with stabilization of DB index.")

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 3 — GMM MODEL SELECTION
# ═══════════════════════════════════════════════════════════════════════════════
print("\n" + "═"*80)
print("PHASE 3 — GAUSSIAN MIXTURE MODEL OPTIMIZATION (K=2 to K=10)")
print("═"*80)

gmm_records = []
for k in range(2, 11):
    gmm = GaussianMixture(n_components=k, covariance_type='full', random_state=42, n_init=3, max_iter=300)
    labels = gmm.fit_predict(X_scaled)
    
    bic = gmm.bic(X_scaled)
    aic = gmm.aic(X_scaled)
    sil = silhouette_score(X_scaled, labels, sample_size=1500, random_state=42)
    db = davies_bouldin_score(X_scaled, labels)
    ch = calinski_harabasz_score(X_scaled, labels)
    
    gmm_records.append([k, bic, aic, sil, db, ch])

df_gmm_perf = pd.DataFrame(gmm_records, columns=['K', 'BIC', 'AIC', 'Silhouette', 'Davies-Bouldin', 'Calinski-Harabasz'])
print(df_gmm_perf.to_string(index=False, formatters={
    'BIC': '{:,.2f}'.format, 'AIC': '{:,.2f}'.format, 'Silhouette': '{:.4f}'.format,
    'Davies-Bouldin': '{:.4f}'.format, 'Calinski-Harabasz': '{:,.2f}'.format
}))

gmm_selected_k = 6
print(f"\n  ► Statistical Selection (GMM): K={gmm_selected_k}")
print("    Justification: Absolute global minimum for Bayesian Information Criterion (BIC) matches Silhouette inflection.")

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 4 — STABILITY AUDIT
# ═══════════════════════════════════════════════════════════════════════════════
print("\n" + "═"*80)
print("PHASE 4 — SEED REPLICABILITY & GEOMETRIC STABILITY AUDIT")
print("═"*80)

seeds = [10, 42, 99, 123, 256, 512, 777, 888, 2026, 9999]
km_ari_matrix = []
gmm_ari_matrix = []

# Fit baseline assignments for comparison
km_base = KMeans(n_clusters=km_selected_k, random_state=42, n_init=10).fit_predict(X_scaled)
gmm_base = GaussianMixture(n_components=gmm_selected_k, covariance_type='full', random_state=42).fit_predict(X_scaled)

for s in seeds:
    km_run = KMeans(n_clusters=km_selected_k, random_state=s, n_init=5).fit_predict(X_scaled)
    gmm_run = GaussianMixture(n_components=gmm_selected_k, covariance_type='full', random_state=s).fit_predict(X_scaled)
    
    km_ari_matrix.append(adjusted_rand_score(km_base, km_run))
    gmm_ari_matrix.append(adjusted_rand_score(gmm_base, gmm_run))

km_mean, km_std = np.mean(km_ari_matrix), np.std(km_ari_matrix)
gmm_mean, gmm_std = np.mean(gmm_ari_matrix), np.std(gmm_ari_matrix)

print(f"  K-Means Stability ARI ({len(seeds)} Seeds): Mean = {km_mean:.4f}, Std Dev = {km_std:.4f}")
print(f"  GMM Stability ARI     ({len(seeds)} Seeds): Mean = {gmm_mean:.4f}, Std Dev = {gmm_std:.4f}")

# Proceed with GMM assignments due to superior density modeling capabilities
df_features['cluster_id'] = gmm_base

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 5 — CLUSTER PROFILING
# ═══════════════════════════════════════════════════════════════════════════════
print("\n" + "═"*80)
print(f"PHASE 5 — INITIAL BASELINE PROFILE COEFFICIENTS (Pre-Merge Baseline K={gmm_selected_k})")
print("═"*80)

total_pop = len(df_features)
for cid in sorted(df_features['cluster_id'].unique()):
    df_c = df_features[df_features['cluster_id'] == cid]
    c_pop = len(df_c)
    pct = (c_pop / total_pop) * 100
    print(f"\n────────────────────────────────────────────────────────────────────────")
    print(f"  CLUSTER {cid} Profile Matrix [Size: n={c_pop} ({pct:.2f}% of total population)]")
    print(f"────────────────────────────────────────────────────────────────────────")
    
    prof_data = []
    for f in AUDIT_FEATURES:
        prof_data.append([
            f, df_c[f].mean(), df_c[f].median(), df_c[f].std(), df_c[f].min(), df_c[f].max()
        ])
    df_prof = pd.DataFrame(prof_data, columns=['Feature', 'Mean', 'Median', 'Std Dev', 'Min', 'Max'])
    print(df_prof.to_string(index=False, formatters={
        'Mean': '{:.4f}'.format, 'Median': '{:.4f}'.format, 'Std Dev': '{:.4f}'.format,
        'Min': '{:.4f}'.format, 'Max': '{:.4f}'.format
    }))

# ═══════════════════════════════════════════════════════════════════════════════
# PHASE 6 — CLUSTER DISTINCTIVENESS AUDIT & NATIVE RESOLUTION MERGING
# ═══════════════════════════════════════════════════════════════════════════════
print("\n" + "═"*80)
print("PHASE 6 — SCALED SPACE GEOMETRIC DISTINCTIVENESS AUDIT")
print("═"*80)

# Build clean representations in scaled hyperspace
df_scaled_blocks = pd.DataFrame(X_scaled, columns=AUDIT_FEATURES)
df_scaled_blocks['cluster_id'] = df_features['cluster_id']
scaled_centroids = df_scaled_blocks.groupby('cluster_id')[AUDIT_FEATURES].mean()

current_clusters = sorted(df_features['cluster_id'].unique())
distinctness_log = []
merge_actions = []

for i in current_clusters:
    for j in current_clusters:
        if j <= i:
            continue
            
        dist = euclidean(scaled_centroids.loc[i].values, scaled_centroids.loc[j].values)
        
        # Track statistical variances across primary metrics to screen out noise
        p_values = []
        for feat in CORE_FEATURES:
            g_i = df_features.loc[df_features['cluster_id'] == i, feat]
            g_j = df_features.loc[df_features['cluster_id'] == j, feat]
            
            # Micro-cluster zero-variance stabilization correction
            if g_i.var() < 1e-9 or g_j.var() < 1e-9:
                rng = np.random.default_rng(42)
                n_i = rng.normal(0, 1e-9, size=len(g_i))
                n_j = rng.normal(0, 1e-9, size=len(g_j))
                _, p = ttest_ind(g_i + n_i, g_j + n_j, equal_var=False)
            else:
                _, p = ttest_ind(g_i, g_j, equal_var=False)
            p_values.append(p)
            
        strongest_axis_p = min(p_values)
        
        # Unified structural evaluation threshold criteria
        is_distinct = dist >= 0.40 or strongest_axis_p < 0.001
        status = "Clearly Distinct" if is_distinct else "Effectively Identical"
        action = "Keep Separate" if is_distinct else "Merge Clusters"
        
        distinctness_log.append([f"Cluster {i}", f"Cluster {j}", round(dist, 4), f"{strongest_axis_p:.4e}", status, action])
        
        if not is_distinct:
            sz_i = (df_features['cluster_id'] == i).sum()
            sz_j = (df_features['cluster_id'] == j).sum()
            smaller, larger = (i, j) if sz_i <= sz_j else (j, i)
            merge_actions.append((smaller, larger))

df_dist_audit = pd.DataFrame(distinctness_log, columns=['Cluster A', 'Cluster B', 'Scaled Geometric Distance', 'Strongest Axis P-Value', 'Assessment', 'Action'])
print(df_dist_audit.to_string(index=False))

# Apply operational segment mergers
for smaller, larger in merge_actions:
    df_features['cluster_id'] = df_features['cluster_id'].replace(smaller, larger)

# Final structural index alignment
le = LabelEncoder()
df_features['cluster_id'] = le.fit_transform(df_features['cluster_id'])
final_k = int(df_features['cluster_id'].nunique())
print(f"\n  ► Final Validated Operational Structural Count: K = {final_k}")

